package com.example.nutri_scan.data;

public class AdditiveItemFetch {
    private String code;
    private String name;

    public AdditiveItemFetch(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }
}
